ORG 3000H
MULT: PUSH AX
PUSH CX
PUSHF

MOV AH, 0

CMP AL, CL
JNS LOOP
PUSH AX
MOV AL, CL
POP CX

CMP CL, 0
JZ FIN_MULT

LOOP: CMP CL, 0
JZ FIN_MULT
ADD AH, AL
DEC CL
JMP LOOP

FIN_MULT: MOV RES, AH
POPF
POP CX
POP AX
RET

ORG 2000H

MOV AL, NUM1
MOV CL, NUM2
CALL MULT
HLT
END

