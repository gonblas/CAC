MULT: PUSH BX
PUSHF

MOV BX, SP
ADD BX, 6
MOV AL, [BX]
ADD BX, 2
MOV BX, [BX]
MOV CL, [BX]


CMP AL, CL
JNS LOOP
PUSH AX
MOV AL, CL
POP CX

MOV AH, 0

CMP CL, 0
JZ FIN_MULT

LOOP: CMP CL, 0
JZ FIN_MULT
ADD AH, AL
DEC CL
JMP LOOP

FIN_MULT: MOV [BX], AH
POPF
POP BX
RET

